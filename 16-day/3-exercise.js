// Crea una variable opcion con valores "1", "2", "3".
// Si es "1": imprime “Agregar producto”
// Si es "2": imprime “Eliminar producto”
// Si es "3": imprime “Salir”

let option = "1";

switch(option){
    case "1": 
        console.log("Agregar producto")
        break;
     case "2" : 
        console.log("Eliminar producto")
        break;
    case "3":
        console.log("salir")
        break;
    
}